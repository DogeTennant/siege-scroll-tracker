def ensure_addon_exists():
    """Write siege_addon.py to disk if it doesn't exist."""
    if not os.path.exists(ADDON_PATH):
        try:
            with open(ADDON_PATH, "w", encoding="utf-8") as f:
                f.write(ADDON_SOURCE)
            return True
        except Exception:
            return False
    return True

