# ── System Checks ────────────────────────────────────────────────────────────

def is_admin():
    try:
        return ctypes.windll.shell32.IsUserAnAdmin() != 0
    except Exception:
        return False


def relaunch_as_admin():
    try:
        ctypes.windll.shell32.ShellExecuteW(
            None, "runas", sys.executable,
            f'"{os.path.abspath(sys.argv[0])}"', None, 1)
    except Exception:
        pass
    sys.exit(0)


def check_mitmdump():
    """Check if mitmdump is available on PATH."""
    try:
        r = subprocess.run(["mitmdump", "--version"],
                          capture_output=True, timeout=5,
                          creationflags=getattr(subprocess, "CREATE_NO_WINDOW", 0))
        return r.returncode == 0
    except Exception:
        return False


def check_cert_file_exists():
    """Check if mitmproxy has generated its CA cert."""
    return os.path.exists(MITMPROXY_CERT)


def generate_cert():
    """Run mitmdump briefly to generate the CA cert files."""
    try:
        p = subprocess.Popen(
            ["mitmdump", "--listen-port", "18999"],
            stdout=subprocess.PIPE, stderr=subprocess.PIPE,
            creationflags=getattr(subprocess, "CREATE_NO_WINDOW", 0))
        time.sleep(2)
        p.terminate()
        p.wait(timeout=5)
    except Exception:
        pass
    return os.path.exists(MITMPROXY_CERT)


def check_cert_installed():
    """Check if mitmproxy CA cert is installed in Windows trust store."""
    try:
        r = subprocess.run(
            ["certutil", "-verifystore", "Root", "mitmproxy"],
            capture_output=True, timeout=10,
            creationflags=getattr(subprocess, "CREATE_NO_WINDOW", 0))
        return r.returncode == 0
    except Exception:
        return False


def install_cert():
    """Install mitmproxy CA cert into Windows Trusted Root store."""
    if not os.path.exists(MITMPROXY_CERT):
        return False
    try:
        r = subprocess.run(
            ["certutil", "-addstore", "Root", MITMPROXY_CERT],
            capture_output=True, timeout=10,
            creationflags=getattr(subprocess, "CREATE_NO_WINDOW", 0))
        return r.returncode == 0
    except Exception:
        return False


# ── Hosts File Management ───────────────────────────────────────────────────

def add_hosts_entry(domain):
    entry = f"127.0.0.1  {domain}  {HOSTS_MARKER}\n"
    try:
        with open(HOSTS_PATH, "r") as f:
            content = f.read()
        if HOSTS_MARKER in content:
            return True
        with open(HOSTS_PATH, "a") as f:
            f.write(entry)
        return True
    except Exception:
        return False


def remove_hosts_entry():
    try:
        with open(HOSTS_PATH, "r") as f:
            lines = f.readlines()
        new_lines = [l for l in lines if HOSTS_MARKER not in l]
        with open(HOSTS_PATH, "w") as f:
            f.writelines(new_lines)
        return True
    except Exception:
        return False


# ── Alliance / Roster Parsing ────────────────────────────────────────────────

def parse_alliance_data(filepath):
    try:
        with open(filepath, "rb") as f:
            data = msgpack.unpackb(f.read(), raw=False, strict_map_key=False)
    except Exception:
        return {}

    members = {}
    if isinstance(data, dict) and "u" in data and isinstance(data["u"], list):
        for profile in data["u"]:
            if isinstance(profile, dict) and "i" in profile and "n" in profile:
                uid = profile["i"]
                name = profile["n"]
                if isinstance(uid, int) and isinstance(name, str) and len(name) > 0:
                    members[str(uid)] = name
    return members


# ── Building Parsing ────────────────────────────────────────────────────────

def building_name_from_id(bid):
    if bid == 1:
        return "Stronghold"
    elif 100 <= bid < 200:
        return f"DefenseTower{bid - 99}"
    elif 1000 <= bid < 2000:
        return f"MagicTower{bid - 1000}"
    elif 2000 <= bid < 3000:
        return f"ManaShrine{bid - 2000}"
    elif 3000 <= bid < 4000:
        return f"Post{bid - 3000}"
    return f"Building{bid}"


def find_attacks_recursive(obj, entries, bname, clan_members):
    if isinstance(obj, dict):
        if ("a" in obj and isinstance(obj["a"], dict) and "i" in obj["a"]
                and "w" in obj and isinstance(obj["w"], bool)):
            uid = str(obj["a"]["i"])
            if uid in clan_members:
                entries.append({"uid": uid, "name": clan_members[uid],
                                "building": bname, "won": obj["w"]})
            return
        for v in obj.values():
            find_attacks_recursive(v, entries, bname, clan_members)
    elif isinstance(obj, list):
        for item in obj:
            find_attacks_recursive(item, entries, bname, clan_members)


def parse_bin_file(filepath, clan_members):
    with open(filepath, "rb") as f:
        data = msgpack.unpackb(f.read(), raw=False, strict_map_key=False)
    bid = 0
    if isinstance(data, dict) and "b" in data and isinstance(data["b"], dict):
        bid = data["b"].get("i", 0)
    bname = building_name_from_id(bid)
    entries = []
    find_attacks_recursive(data, entries, bname, clan_members)
    return entries, bname, bid


def parse_all_buildings(search_dir, clan_members):
    bin_files = sorted(glob.glob(os.path.join(search_dir, "building_*.bin")))
    bin_files = [f for f in bin_files if not f.endswith("_req.bin")]

    all_entries, buildings, seen_ids = [], [], set()
    for fpath in bin_files:
        try:
            entries, bname, bid = parse_bin_file(fpath, clan_members)
            if bid in seen_ids:
                continue
            seen_ids.add(bid)
            all_entries.extend(entries)
            buildings.append(bname)
        except Exception:
            pass

    stats = {}
    for uid, name in clan_members.items():
        stats[uid] = {"name": name, "scrolls": 0, "wins": 0,
                       "losses": 0, "buildings": set()}
    for entry in all_entries:
        uid = entry["uid"]
        if uid not in stats:
            stats[uid] = {"name": entry["name"], "scrolls": 0,
                           "wins": 0, "losses": 0, "buildings": set()}
        stats[uid]["scrolls"] += 1
        if entry["won"]:
            stats[uid]["wins"] += 1
        else:
            stats[uid]["losses"] += 1
        stats[uid]["buildings"].add(entry["building"])

    bldg_counts = defaultdict(lambda: [0, 0])
    for e in all_entries:
        bldg_counts[e["building"]][0] += 1
        if e["won"]:
            bldg_counts[e["building"]][1] += 1

    return stats, buildings, bldg_counts


