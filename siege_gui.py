    # ── Setup Checks ─────────────────────────────────────────────────────

    def _run_setup_check(self):
        """Check all requirements and update status indicators."""
        checks = {}

        # Admin
        checks["admin"] = is_admin()
        self._update_check("admin", "Admin", checks["admin"])

        # mitmdump
        checks["mitmdump"] = check_mitmdump()
        self._update_check("mitmdump", "mitmproxy", checks["mitmdump"])

        # Certificate
        cert_installed = check_cert_installed()
        if cert_installed:
            checks["cert"] = True
        elif check_cert_file_exists():
            checks["cert"] = False  # Exists but not installed
        else:
            checks["cert"] = False
        self._update_check("cert", "Certificate", checks["cert"])

        # Addon file
        checks["addon"] = os.path.exists(ADDON_PATH)
        self._update_check("addon", "Addon", checks["addon"])

        # Show fix button if issues
        has_issues = not all(checks.values())
        if has_issues:
            self.fix_btn.pack(side="right")
            if not checks["mitmdump"]:
                self._set_status("Setup needed — install mitmproxy first", self.yellow)
            elif not checks["cert"]:
                self._set_status("Setup needed — click Fix Issues to install certificate", self.yellow)
            elif not checks["admin"]:
                self._set_status("Not running as Admin — restart with admin rights", self.yellow)
        else:
            self.fix_btn.pack_forget()
            self._set_status("Ready — click Start Capture to begin", self.text_dim)

        # Enable/disable start button
        can_start = checks["admin"] and checks["mitmdump"] and checks["cert"] and checks["addon"]
        self.start_btn.configure(state="normal" if can_start else "disabled")

    def _update_check(self, key, label, ok):
        color = self.green if ok else self.red
        icon = "✓" if ok else "✗"
        self.setup_labels[key].configure(
            text=f"{icon} {label}", foreground=color)

    def _fix_issues(self):
        """Attempt to fix setup issues automatically."""
        messages = []

        if not check_mitmdump():
            messages.append(
                "mitmproxy is not installed.\n\n"
                "Download and install from:\n"
                "https://mitmproxy.org/\n\n"
                "After installing, restart this application."
            )
            messagebox.showinfo("Install mitmproxy", messages[0])
            return

        if not check_cert_file_exists():
            self._set_status("Generating mitmproxy certificate...", self.yellow)
            self.root.update()
            generate_cert()

        if check_cert_file_exists() and not check_cert_installed():
            answer = messagebox.askyesno(
                "Install Certificate",
                "mitmproxy's CA certificate needs to be trusted by Windows\n"
                "so the tool can read game traffic.\n\n"
                "This is safe — it only allows this tool to inspect\n"
                "Raid's connection. Install the certificate now?\n\n"
                f"Certificate: {MITMPROXY_CERT}")
            if answer:
                self._set_status("Installing certificate...", self.yellow)
                self.root.update()
                if install_cert():
                    messagebox.showinfo("Success",
                        "Certificate installed successfully!")
                else:
                    messagebox.showerror("Failed",
                        "Could not install certificate.\n"
                        "Make sure you're running as Administrator.")

        if not is_admin():
            answer = messagebox.askyesno("Admin Required",
                "This app needs administrator privileges.\n"
                "Restart with admin rights?")
            if answer:
                relaunch_as_admin()

        if not os.path.exists(ADDON_PATH):
            ensure_addon_exists()
            if not os.path.exists(ADDON_PATH):
                messagebox.showerror("Missing Addon",
                    f"Could not create siege_addon.py at:\n{ADDON_PATH}\n\n"
                    "Check folder write permissions.")

        self._run_setup_check()

